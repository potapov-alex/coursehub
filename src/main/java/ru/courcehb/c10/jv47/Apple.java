package ru.courcehb.c10.jv47;

public class Apple {
    private int appleX;
    private int appleY;

    public Apple(int appleX, int appleY) {
        this.appleX = appleX;
        this.appleY = appleY;
    }

    public int getAppleX() {
        return appleX;
    }

    public int getAppleY() {
        return appleY;
    }
}